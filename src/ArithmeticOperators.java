public class ArithmeticOperators {
    public static void main(String[] args) {
        //        Arithmetic (%)
        int angka = 100, penambah = 50, pengurang = 20, pengali = 5, pembagi = 25, modulus = 15;
        System.out.println("KALKULATOR ARITMATIKA");
        System.out.println("==========================================");
        System.out.println("Angka awal adalah " + angka);
        System.out.println("Penjumlahan: " + angka + " dengan " + penambah + " adalah " + (angka + penambah));
        System.out.println("Pengurangan: " + angka + " dengan " + pengurang + " adalah " + (angka - pengurang));
        System.out.println("Perkalian: " + angka + " dengan " + pengali + " adalah " + (angka * pengali));
        System.out.println("Pembagian: " + angka + " dengan " + pembagi + " adalah " + (angka / pembagi));
        System.out.println("Hasil modulus: " + angka + " dengan " + modulus + " adalah " + (angka % modulus));

    }
}
